     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $this->validate($request, [
            'profile_img' => 'image|nullable|max:1999'
        ]);

        if($request->hasFile('profile_img')){
            // Get filename with the extension
            $filenameWithExt = $request->file('profile_img')->getClientOriginalName();
            // Get just filename
            $filename = pathinfo($filenameWithExt, PATHINFO_FILENAME);
            // Get just ext
            $extension = $request->file('profile_img')->getClientOriginalExtension();
            // Filename to store
            $fileNameToStore= $filename.'_'.time().'.'.$extension;
            // Upload Image
            $path = $request->file('profile_img')->storeAs('public/profile_imgs', $fileNameToStore);
        } else {
            $fileNameToStore = 'noimage.jpg';
        }
        $user = User::find($id);
        $user->profile_img = $fileNameToStore;
        $user->save();
        return view('/profile');
    }

